package Trips;

public class Trips {
	
	int user;
	String startDate;
	String trail;
	String campsite;
	String rName;
	String rPhone;
	
	public Trips(int user, String startDate, String trail, String campsite, String rName, String rPhone) {
		super();
		this.user = user;
		this.startDate = startDate;
		this.trail = trail;
		this.campsite = campsite;
		this.rName = rName;
		this.rPhone = rPhone;
	}

}
